package aster

import (
	"reflect"
)

// Type is the representation of a Go type.
type Type interface {
	// Methods applicable to all types.

	// Method returns the i'th method in the type's method set.
	// It panics if i is not in the range [0, NumMethod()).
	//
	// For a non-interface type T or *T, the returned Method's Type and Func
	// fields describe a function whose first argument is the receiver.
	//
	// For an interface type, the returned Method's Type field gives the
	// method signature, without a receiver, and the Func field is nil.
	Method(int) Method

	// MethodByName returns the method with that name in the type's
	// method set and a boolean indicating if the method was found.
	//
	// For a non-interface type T or *T, the returned Method's Type and Func
	// fields describe a function whose first argument is the receiver.
	//
	// For an interface type, the returned Method's Type field gives the
	// method signature, without a receiver, and the Func field is nil.
	MethodByName(string) (Method, bool)

	// NumMethod returns the number of exported methods in the type's method set.
	NumMethod() int

	// Name returns the type's name within its package for a defined type.
	// For other (non-defined) types it returns the empty string.
	Name() string

	// PkgPath returns a defined type's package path, that is, the import path
	// that uniquely identifies the package, such as "encoding/base64".
	// If the type was predeclared (string, error) or not defined (*T, struct{},
	// []int, or A where A is an alias for a non-defined type), the package path
	// will be the empty string.
	PkgPath() string

	// String returns a string representation of the type.
	// The string representation may use shortened package names
	// (e.g., base64 instead of "encoding/base64") and is not
	// guaranteed to be unique among types. To test for type identity,
	// compare the Types directly.
	String() string

	// Kind returns the specific kind of this type.
	Kind() Kind

	// Implements reports whether the type implements the interface type u.
	Implements(u Type) bool

	// AssignableTo reports whether a value of the type is assignable to type u.
	AssignableTo(u Type) bool

	// ConvertibleTo reports whether a value of the type is convertible to type u.
	ConvertibleTo(u Type) bool

	// Comparable reports whether values of this type are comparable.
	Comparable() bool

	// ChanDir returns a channel type's direction.
	// It panics if the type's Kind is not Chan.
	ChanDir() ChanDir

	// IsVariadic reports whether a function type's final input parameter
	// is a "..." parameter. If so, t.In(t.NumIn() - 1) returns the parameter's
	// implicit actual type []T.
	//
	// For concreteness, if t represents func(x int, y ... float64), then
	//
	//	t.NumIn() == 2
	//	t.In(0) is the Type for "int"
	//	t.In(1) is the Type for "[]float64"
	//	t.IsVariadic() == true
	//
	// IsVariadic panics if the type's Kind is not Func.
	IsVariadic() bool

	// Elem returns a type's element type.
	// It panics if the type's Kind is not Array, Chan, Map, Ptr, or Slice.
	Elem() Type

	// Field returns a struct type's i'th field.
	// It panics if the type's Kind is not Struct.
	// It panics if i is not in the range [0, NumField()).
	Field(i int) StructField

	// FieldByIndex returns the nested field corresponding
	// to the index sequence. It is equivalent to calling Field
	// successively for each index i.
	// It panics if the type's Kind is not Struct.
	FieldByIndex(index []int) StructField

	// FieldByName returns the struct field with the given name
	// and a boolean indicating if the field was found.
	FieldByName(name string) (StructField, bool)

	// FieldByNameFunc returns the struct field with a name
	// that satisfies the match function and a boolean indicating if
	// the field was found.
	//
	// FieldByNameFunc considers the fields in the struct itself
	// and then the fields in any embedded structs, in breadth first order,
	// stopping at the shallowest nesting depth containing one or more
	// fields satisfying the match function. If multiple fields at that depth
	// satisfy the match function, they cancel each other
	// and FieldByNameFunc returns no match.
	// This behavior mirrors Go's handling of name lookup in
	// structs containing embedded fields.
	FieldByNameFunc(match func(string) bool) (StructField, bool)

	// In returns the type of a function type's i'th input parameter.
	// It panics if the type's Kind is not Func.
	// It panics if i is not in the range [0, NumIn()).
	In(i int) Type

	// Key returns a map type's key type.
	// It panics if the type's Kind is not Map.
	Key() Type

	// Len returns an array type's length.
	// It panics if the type's Kind is not Array.
	Len() int

	// NumField returns a struct type's field count.
	// It panics if the type's Kind is not Struct.
	NumField() int

	// NumIn returns a function type's input parameter count.
	// It panics if the type's Kind is not Func.
	NumIn() int

	// NumOut returns a function type's output parameter count.
	// It panics if the type's Kind is not Func.
	NumOut() int

	// Out returns the type of a function type's i'th output parameter.
	// It panics if the type's Kind is not Func.
	// It panics if i is not in the range [0, NumOut()).
	Out(i int) Type

	// Source returns the source code.
	Source() string
	// GetDoc returns lead comment.
	GetDoc() string
	// GetComment returns line comment.
	GetComment() string
	// SetDoc sets lead comment.
	SetDoc(string)
	// SetComment sets line comment.
	SetComment(string)
}

// A Kind represents the specific kind of type that a Type represents.
// The zero Kind is not a valid kind.
type Kind = reflect.Kind

// Kind enumerate
const (
	Invalid       Kind = reflect.Invalid
	Bool          Kind = reflect.Bool
	Int           Kind = reflect.Int
	Int8          Kind = reflect.Int8
	Int16         Kind = reflect.Int16
	Int32         Kind = reflect.Int32
	Int64         Kind = reflect.Int64
	Uint          Kind = reflect.Uint
	Uint8         Kind = reflect.Uint8
	Uint16        Kind = reflect.Uint16
	Uint32        Kind = reflect.Uint32
	Uint64        Kind = reflect.Uint64
	Uintptr       Kind = reflect.Uintptr
	Float32       Kind = reflect.Float32
	Float64       Kind = reflect.Float64
	Complex64     Kind = reflect.Complex64
	Complex128    Kind = reflect.Complex128
	Array         Kind = reflect.Array
	Chan          Kind = reflect.Chan
	Func          Kind = reflect.Func
	Interface     Kind = reflect.Interface
	Map           Kind = reflect.Map
	Ptr           Kind = reflect.Ptr
	Slice         Kind = reflect.Slice
	String        Kind = reflect.String
	Struct        Kind = reflect.Struct
	UnsafePointer Kind = reflect.UnsafePointer
)

// ChanDir represents a channel type's direction.
type ChanDir = reflect.ChanDir

// ChanDir enumerate
const (
	RecvDir ChanDir = reflect.RecvDir // <-chan
	SendDir ChanDir = reflect.SendDir // chan<-
	BothDir ChanDir = reflect.BothDir // chan
)

// Method represents a single method.
type Method struct {
	Name     string // method name
	Exported bool   // is upper case (exported) method name or not
	Type     Type   // method type
	Func     string // func raw value string
	Index    int    // index for Type.Method
	Doc      string // lead comment
}

// A StructField describes a single field in a struct.
type StructField struct {
	Name      string    // the field name
	Exported  bool      // is upper case (exported) field name or not
	Type      Type      // field type
	Tag       StructTag // field tag string
	Index     []int     // index sequence for Type.FieldByIndex
	Anonymous bool      // is an embedded field
	Doc       string    // lead comment
	Comment   string    // line comment
}

// A StructTag is the tag string in a struct field.
//
// By convention, tag strings are a concatenation of
// optionally space-separated key:"value" pairs.
// Each key is a non-empty string consisting of non-control
// characters other than space (U+0020 ' '), quote (U+0022 '"'),
// and colon (U+003A ':').  Each value is quoted using U+0022 '"'
// characters and Go string literal syntax.
type StructTag = reflect.StructTag
